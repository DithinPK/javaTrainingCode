
public class MultiDimensionalArray {

	public static void main(String[] args) {

		int[][] grid = { { 27, 12, 99 }, 
				         { 10, 17 }, 
				         { 19, 12, 11, 31 } };
		
		System.out.println(grid[0][1]);
		System.out.println(grid[0][2]);
		System.out.println(grid[1][1]);
		System.out.println(grid[2][3]);

	}

}


// 7 12 99
// 10 17
// 19 12 11 31