
public class IfConditions {

	public static void main(String[] args) {

		int value = 15;

		if (value < 10)
			System.out.println("Value " + value + " is less than 10");
		else if (value > 20)
			System.out.println("Value " + value + " is greater than 20");
		else
			System.out.println("None of the above");
	}
}
